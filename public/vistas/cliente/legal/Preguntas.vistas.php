                    <h4> 3. Qué materiales se utilizan en la bisutería </h4>
                </a>
                <div id="collapseThree" class="collapse" data-parent="#accordion">
                    <p>
                        En la bisutería, se utilizan una amplia variedad de materiales para crear distintas
                        piezas. Algunos de los materiales más comunes son:<br>
                        <strong>Cuentas y piedras:</strong> Se utilizan cuentas de diferentes materiales como
                        vidrio, cerámica,
                        madera, plástico, metal, semillas, entre otros.
                        También se utilizan piedras semipreciosas como cuarzo, amatista, jade, turquesa, entre
                        otros.<br>
                        <strong>Metales:</strong> Se utilizan diferentes tipos de metales como latón, cobre,
                        aluminio, acero
                        inoxidable, zamak, entre otros.
                        Estos metales se utilizan principalmente para hacer cadenas, dijes, anillos, broches y
                        cierres.<br>
                        <strong>Hilos y cordones:</strong> Se utilizan hilos o cordones de diferentes materiales
                        como algodón,
                        seda, nylon, cuero, goma, entre otros.
                        Estos se utilizan para ensartar las cuentas y crear collares, pulseras y otros
                        accesorios.<br>
                        <strong>Cristales y perlas:</strong> Se utilizan cristales de diferentes formas y
                        tamaños, como cristal
                        de Swarovski, así como perlas de vidrio
                        o de agua dulce, para crear piezas más elegantes y sofisticadas.<br>
                        <strong>Charms y dijes:</strong> Se utilizan dijes y charms fabricados en diferentes
                        materiales como
                        metal esmaltado, resinas, vidrio, porcelana, entre otros.
                        Estos se utilizan principalmente en pulseras y collares.<br>
                        <strong>Cierres y broches: </strong>Se utilizan diferentes tipos de cierres y broches,
                        como mosquetones,
                        imanes, corchetes, entre otros, para poder
                        ajustar las pulseras, collares y otros accesorios.<br>
                        Estos son solo algunos ejemplos de los materiales más utilizados en la bisutería, pero
                        hay una gran variedad de opciones y combinaciones
                        posibles para crear piezas únicas y personalizadas.
