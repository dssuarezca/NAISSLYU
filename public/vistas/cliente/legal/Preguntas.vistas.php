                    <h4>2. ¿Cuál es la diferencia entre bisutería y joyería? </h4>
                
                </a>
                <div id="collapseTwo" class="collapse" data-parent="#accordion">
                    <p>
                        La diferencia principal entre bisutería y joyería radica en los materiales utilizados y
                        el proceso de fabricación.
                        La bisutería se elabora con materiales menos costosos como aleaciones de metales,
                        plásticos, vidrios, piedras
                        sintéticas, entre otros. Su proceso de fabricación suele ser industrializado, con piezas
                        producidas en masa.
                        La bisutería tiende a ser más accesible en términos de precio y está diseñada para
                        satisfacer las tendencias
                        de moda pasajeras.
                        Por otro lado, la joyería se elabora con materiales valiosos como oro, plata, platino y
                        piedras preciosas o
                        semi preciosas como diamantes, rubíes, zafiros, perlas, entre otros. Su proceso de
                        fabricación es de alta calidad
                        y suele ser realizado a mano por artesanos especializados. La joyería es más duradera,
                        valiosa y se considera
                        una inversión a largo plazo, ya que su valor puede aumentar con el tiempo.
                    </p>
