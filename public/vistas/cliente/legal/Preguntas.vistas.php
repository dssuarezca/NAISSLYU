                            4. Cuánto tiempo suele durar la bisutería
                        </h4>
                    
                </a>
                <div id="collapseFour" class="collapse" data-parent="#accordion">
                    <p>
                        La duración de la bisutería puede variar dependiendo de factores, como la calidad de los
                        materiales utilizados, el cuidado y mantenimiento <br>
                        que se le brinde, y la frecuencia de uso. En general, la bisutería tiende a ser menos
                        duradera que las joyas de alta calidad. Algunas piezas <br>
                        de bisutería pueden deteriorarse o perder su brillo con el tiempo, especialmente si
                        están expuestas a la humedad, productos químicos o golpes. <br>
                        Sin embargo, si se les da un buen cuidado y se almacenan adecuadamente, algunas piezas
                        de bisutería pueden durar varios años.<br>
                    </p>
